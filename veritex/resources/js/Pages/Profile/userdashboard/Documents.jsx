  const [currentPage, setCurrentPage] = useState(1);

  // Split content into pages (simulate pages of ~600 chars)
  const lines = doc.fullContent.split('\n');
  const charsPerPage = 1200;
  const pages = [];
  let current = '';
  for (const line of lines) {
    if (current.length + line.length > charsPerPage && current.length > 0) {
      pages.push(current.trim());
      current = line + '\n';
    } else {
      current += line + '\n';
    }
  }
  if (current.trim()) pages.push(current.trim());
  const totalPages = Math.max(pages.length, doc.pages);
  const pageContent = pages[currentPage - 1] || `[Page ${currentPage} — continued]`;

